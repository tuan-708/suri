import 'package:suri_checking_event_app/features/event/domain/entities/event_register_entity.dart';

class EventRegisterModel extends EventRegisterEntity {
  const EventRegisterModel({
    required int id,
    required int active,
    required String name,
    required String info,
    required String description,
    required int eventId,
    required int accountId,
    required int eventAccountTypeId,
    int? typeAccount,
    String? address,
    String? phone,
    String? photo,
    required DateTime createdTime,
    String? gmail,
    String? note,
    String? relationship,
    String? account,
    String? event,
    String? eventAccountType,
    required List<dynamic> eventAccountCheckins,
  }) : super(
          id: id,
          active: active,
          name: name,
          info: info,
          description: description,
          eventId: eventId,
          accountId: accountId,
          eventAccountTypeId: eventAccountTypeId,
          typeAccount: typeAccount,
          address: address,
          phone: phone,
          photo: photo,
          createdTime: createdTime,
          gmail: gmail,
          note: note,
          relationship: relationship,
          account: account,
          event: event,
          eventAccountType: eventAccountType,
          eventAccountCheckins: eventAccountCheckins,
        );

  factory EventRegisterModel.fromJson(Map<String, dynamic> json) {
    return EventRegisterModel(
      id: json['id'],
      active: json['active'],
      name: json['name'] ?? "",
      info: json['info'] ?? "",
      description: json['description'] ?? "",
      eventId: json['eventId'],
      accountId: json['accountId'],
      eventAccountTypeId: json['eventAccountTypeId'],
      typeAccount: json['typeAccount'],
      address: json['address'] ?? "",
      phone: json['phone'] ?? "",
      photo: json['photo'] ?? "",
      createdTime: DateTime.parse(json['createdTime']),
      gmail: json['gmail'] ?? "",
      note: json['note'] ?? "",
      relationship: json['relationship'] ?? "",
      account: json['account'] ?? "",
      event: json['event'] ?? "",
      eventAccountType: json['eventAccountType'] ?? "",
      eventAccountCheckins: List<dynamic>.from(json['eventAccountCheckins']),
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'active': active,
      'name': name,
      'info': info,
      'description': description,
      'eventId': eventId,
      'accountId': accountId,
      'eventAccountTypeId': eventAccountTypeId,
      'typeAccount': typeAccount,
      'address': address,
      'phone': phone,
      'photo': photo,
      'createdTime': createdTime.toIso8601String(),
      'gmail': gmail,
      'note': note,
      'relationship': relationship,
      'account': account,
      'event': event,
      'eventAccountType': eventAccountType,
      'eventAccountCheckins': eventAccountCheckins,
    };
  }
}
