import 'package:suri_checking_event_app/features/gift/domain/entities/event_gift_entity.dart';

class EventGiftModel extends EventGiftEntity {
  const EventGiftModel({
    required String? giftName,
    required String? eventName,
    required String? eventPhoto,
    required String? eventInfo,
    required DateTime? eventStartDate,
    required DateTime? eventEndDate,
    required String? photo,
    required double? price,
    required bool? isSender,
    required String? eventGiftTypeName,
    required int? id,
    required int? active,
    int? quantity,
    required String? name,
    String? info,
    String? description,
    required DateTime? createdTime,
    required int? giftId,
    required int? eventId,
    required int? eventGiftTypeId,
    dynamic event,
    dynamic gift,
    dynamic eventGiftType,
    required List<dynamic> accountGifts,
  }) : super(
          giftName: giftName,
          eventName: eventName,
          eventPhoto: eventPhoto,
          eventInfo: eventInfo,
          eventStartDate: eventStartDate,
          eventEndDate: eventEndDate,
          photo: photo,
          price: price,
          isSender: isSender,
          eventGiftTypeName: eventGiftTypeName,
          id: id,
          active: active,
          quantity: quantity,
          name: name,
          info: info,
          description: description,
          createdTime: createdTime,
          giftId: giftId,
          eventId: eventId,
          eventGiftTypeId: eventGiftTypeId,
          event: event,
          gift: gift,
          eventGiftType: eventGiftType,
          accountGifts: accountGifts,
        );

  factory EventGiftModel.fromJson(Map<String, dynamic> json) {
    return EventGiftModel(
      giftName: json['giftName'] ?? '',
      eventName: json['eventName'] ?? '',
      eventPhoto: json['eventPhoto'] ?? '',
      eventInfo: json['eventInfo'] ?? '',
      eventStartDate: DateTime.parse(json['eventStartDate']),
      eventEndDate: DateTime.parse(json['eventEndDate']),
      photo: json['photo'] ?? '',
      price: json['price'] ,
      isSender: json['isSender'],
      eventGiftTypeName: json['eventGiftTypeName'] ?? '',
      id: json['id'],
      active: json['active'],
      quantity: json['quantity'],
      name: json['name'] ?? '',
      info: json['info'] ?? '',
      description: json['description'] ?? '',
      createdTime: DateTime.parse(json['createdTime']),
      giftId: json['giftId'],
      eventId: json['eventId'],
      eventGiftTypeId: json['eventGiftTypeId'],
      event: json['event'],
      gift: json['gift'],
      eventGiftType: json['eventGiftType'],
      accountGifts: List<dynamic>.from(json['accountGifts']),
    );
  }
}
